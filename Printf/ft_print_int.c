#include "printf.h"

int	ft_print_int(int num)
{
	int		i;
	char	*s;

	i = 0;
	s = ft_itoa(num);
	while (s[i])
	{
		ft_putchar(s[i]);
		i++;
	}	
	free(s);
	return (i);
}
